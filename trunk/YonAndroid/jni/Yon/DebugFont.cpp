	void DebugFont::drawString(const core::stringc& str,const core::position2di& pos){
		core::position2di p(pos);
		if(m_pDriver){
			s32 r,d;
			for(u32 i=0;i<str.length();++i){
				d=(str[i]-32)/CHAR_COUNT_PER_ROW;
				r=(str[i]-32)%CHAR_COUNT_PER_ROW;
				m_pDriver->draw2DImage(m_pTexture,p,texcoords[d][r]);
				//Logger->debug("%c,%d,%d,%d\n",str[i],str[i],d,r);
				//texcoords[d][r].print();
				p.x+=CHAR_SIZE;
			}
		}
	}
